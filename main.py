while True:
    choice = input("Press 1 for letter to morse translation. Press 2 for morse to letter translation.")
    if choice == "1":
        message = input("Insert text to translate").upper()
        translated_message = ""

        for i in message:
            if i in letter_to_morse:
                translated_message += letter_to_morse[i] + " "
            else:
                translated_message += "# "
        print(translated_message)

    elif choice == "2":
        message = input("Insert morse code to translate")
        splitted_message = message.split()
        translated_message = ""

